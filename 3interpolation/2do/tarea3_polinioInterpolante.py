p1 = (-1, 3)
p2 = (0, -7)
p3 = (4, 7)
p4 = (5, 11)

#valor a buscar en x
x = 3

def fn():
    a = ((x - p2[0]) / (p1[0] - p2[0]))
    b = ((x - p3[0]) / (p1[0] - p3[0]))
    c = ((x - p4[0]) / (p1[0] - p4[0]))
    r1 = a * b * c * p1[1]

    a1 = ((x - p1[0]) / (p2[0] - p1[0]))
    b1 = ((x - p3[0]) / (p2[0] - p3[0]))
    c1 = ((x - p4[0]) / (p2[0] - p4[0]))
    r2 = a1 * b1 * c1 * p2[1]

    a2 = ((x - p1[0]) / (p3[0] - p1[0]))
    b2 = ((x - p2[0]) / (p3[0] - p2[0]))
    c2 = ((x - p4[0]) / (p3[0] - p4[0]))
    r3 = a2 * b2 * c2 * p3[1]

    a3 = ((x - p1[0]) / (p4[0] - p1[0]))
    b3 = ((x - p2[0]) / (p4[0] - p2[0]))
    c3 = ((x - p3[0]) / (p4[0] - p3[0]))
    r4 = a3 * b3 * c3 * p4[1]

    return r1 + r2 + r3 + r4

print("x=", x, " y=", fn())